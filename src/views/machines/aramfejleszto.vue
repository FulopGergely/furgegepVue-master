<h1>Termékleírás</h1>
<p> A három 230 V-os aljzatot lehet használni standard egyfázisú eszközök áram ellátásához.
  Ez az eszköz szinte bárhol használható, és a folyamatos 400 watt teljesítménye elegendő sok alkalmazáshoz.
   A készülék túlterhelés elleni védelemmel van ellátva. Az automatikus (AVR) feszültségszabályozás megakadályozza a
    feszültség ingadozását, így stabil üzemre képes. </p> 
    <p> A 4 ütemű HECHT benzinmotor 420 cm3 hengerűrtartalommal, 10,7 LE teljesítmény leadására képes. Egy teli tank, 28 liter 
üzemanyaggal körülbelül 27 óra folyamatos áramtermelésre képes.  Ez az adat a terhelés függvényében módosulhat.  </p> 
<p> A HECHT GG 5000 generátor indítása egy hagyományos berántó szerkezet által történik. A motor és az elektronika egy acél 
cső keretben van elhelyezve, futó kerekekkel és mozgatható fogantyúval. Ezáltal nagyon könnyű mozgatni a 65 kg-os gépet.
A fent említett paraméterek alapján alkalmazható épületek energiaforrásaként való használatra. Egy tökéletes biztonsági 
tartalék áramforrás áramszünet esetére.  </p> 

<p> Az áramellátás számításakor az alábbi tényezőket is figyelembe kell venni:</p> 
<ul class="list-group">
   <li class="list-group-item">Inverteres hegesztőgépek esetén a javasolt együttható 1,25-1,5. </li>
   <li class="list-group-item">UPS (szünetmentes tápegységek) esetén az együttható 1-2,3 között változhat, a pontos értéket a szünetmentes tápegység műszaki adatainak ismeretében kell kiválasztani. </li>
   <li class="list-group-item">Amennyiben semmilyen információ nem áll rendelkezésre, 1,8-2 közötti értékkel ajánlott számolni.</li>
</ul>  


<p>Elektromos motorok esetén: Az elektromos motorok nagy indítási áramot igényelnek, kellő pontossággal megbecsülhető ez az érték: ismernünk kell a motor teljesítményét (Watt)</p>
<ul class="list-group">
   <li class="list-group-item">kefés motorok (fúrógépek, sarokcsiszolók, stb.) esetén az együttható 1,4  </li>
   <li class="list-group-item">aszinkron motorok esetén (kompresszor, betonkeverő,stb.) az együttható 2,5-3</li>
   <li class="list-group-item">Például egy 1,5 kW teljesítményű motorral felszerelt betonkeverő teljesítmény igénye így 1,5 kW X 2,5 = 3,75 kW</li>
</ul>  

<p>Indítási kapacitás: Az elektromos motorok az indítás ideje alatt a névleges áramfelvitel többszörösét is felveszik. 
Ha a fogyasztó nagy indítóáramot igényel az áramfejlesztő/aggregátor teljesítményének legalább 2-3-szorosan kell meghaladnia 
a róla üzemeltetett elektromos motor teljesítményét. Az indítóáram szempontjából legkedvezőtlenebbek a gyakran indított, nagy
áramot felvevő elektromos motorok, pl.: szivattyúk, légkompresszorok, liftek, betonkeverő, vakológép, stb. Ilyenkor előfordulhat,
 akár 6-9-szeres áramfelvétel, amelyet az áramfejlesztőnek elő kell állítani, még ha rövid időre is. Ellenkező esetben a motor nem indul el. </p>
